async function buildIconModules(pkgDir) {
    console.log(`🛠️  Generating icons for ${pkgDir}...`);
    const pkgName = path.basename(pkgDir);
    const iconsDir = path.join(pkgDir, "icons");
    const setName = pkgName.replace(/^icon-/, "");
    const srcDir = path.join(pkgDir, "src");
    const outIconsDir = path.join(srcDir, "icons");
    const distDir = path.join(pkgDir, "dist");

    if (!fs.existsSync(iconsDir)) return;

    fs.mkdirSync(outIconsDir, { recursive: true });
    fs.mkdirSync(distDir, { recursive: true });

    // Recursively collect SVG files, preserving relative paths
    function walk(dir, base = "") {
        const entries = fs.readdirSync(dir, { withFileTypes: true });
        const out = [];
        for (const e of entries) {
            const rel = path.posix.join(base || "", e.name);
            const full = path.join(dir, e.name);
            if (e.isDirectory()) {
                out.push(...walk(full, rel));
            } else if (e.isFile() && e.name.endsWith(".svg")) {
                out.push(rel); // rel like "24/outline/home.svg"
            }
        }
        return out;
    }

    const relSvgFiles = walk(iconsDir);
    const exportLines = [];
    const globalExports = [];
    const globalNames = [];
    const licenseInfo = getLicense(pkgName);

    console.log(`🛠️  Building ${relSvgFiles.length} icons for ${pkgName}...`);

    for (const relPath of relSvgFiles) {
        const svgContent = fs.readFileSync(path.join(iconsDir, relPath), "utf8").trim();
        const optimized = optimizeSVG(svgContent);
        const withLicense = insertLicense(optimized, licenseInfo);

        const dirRel = path.posix.dirname(relPath); // e.g. "24/outline" or "."
        const fileBase = path.posix.basename(relPath); // e.g. "home.svg"
        const baseName = sanitizeName(fileBase.replace(/\.svg$/, "").replace(/_/g, '-')); // "home"
        // const iconKey = dirRel === "." ? baseName : `${dirRel}/${baseName}`; // e.g. "24/outline/home"
        const iconKey = dirRel === "." ? baseName : `${baseName}-${dirRel}`; //e.g. "home-outline-24"

        const pascalName = toPascalCase(iconKey.replace(/\//g, "-"));
        const outDirForFile = dirRel === "." ? outIconsDir : path.join(outIconsDir, dirRel);
        fs.mkdirSync(outDirForFile, { recursive: true });

        const outPath = path.join(outDirForFile, `${baseName}.ts`);
        await generateIconDefinitions(withLicense, {iconKey, baseName, pascalName, outPath, licenseInfo})
        exportLines.push(`export { default as Icon${pascalName} } from './icons/${iconKey}.js';`);
        globalExports.push(`  "${iconKey}": icons.Icon${pascalName},`);
        globalNames.push(iconKey);
    }

    // Write index.ts with preserved keys (including subpaths)
    // fs.writeFileSync(
    //     path.join(srcDir, "index.ts"),
    //     exportLines.join("\n") + "\n" + `export type ${toPascalCase(setName)}IconName = ${globalNames.map((n) => `"${n}"`).join(" | ")};`
    // );

    await generateAllIcons({srcDir, pkgName, setName, relSvgFiles, globalExports});

    // icons-map.ts using dynamic imports (preserves subpaths)
    await generateIconMap({srcDir, pkgName, setName, globalNames, licenseInfo});
    await generateIconImportMap({srcDir, pkgName, setName, globalNames, licenseInfo});

    console.log(`✅ ${pkgName} ready (TS + license inline).`);
}

async function generateIconDefinitions(code, options){
    const {pascalName, licenseInfo, iconKey, outPath} = options
    const tsCode = `\
    /**
     * Auto-generated by generate-icon-modules.ts
     * Icon: ${pascalName}
     * License: ${licenseInfo}
     */
    const Icon${pascalName} = \`${code}\`;
    export default Icon${pascalName};
    export { Icon${pascalName} };
    export type IconName = '${iconKey}'`;
    const formatted_code = await formatCode(tsCode, {parser: 'typescript'})
    
    // fs.writeFileSync(outPath, tsCode.trim() + "\n");
}

async function generateAllIcons(options){
    const {srcDir, pkgName, setName, relSvgFiles, globalExports} = options   
    const allIconsPath = path.join(srcDir, "all-icons.ts");
    const bundleCode = `\
        /**
         * Auto-generated global registry for ${pkgName}
         * Adds icons to window.FLUIXI_ICONS['${setName}'] without overwriting others.
         */
        import * as icons from './index.js';

        declare global {
            interface Window {
                FLUIXI_ICONS?: Record<string, Record<string, string>>;
            }
        }

        if (typeof window !== 'undefined') {
            window.FLUIXI_ICONS = window.FLUIXI_ICONS || {};
            window.FLUIXI_ICONS['${setName}'] = {
            ${globalExports.join("\n\t")}
            };
            console.log('[Fluixi Icons] Loaded ${relSvgFiles.length} icons from ${pkgName}.');
        }
        export default window?.FLUIXI_ICONS?.['${setName}'];
        `;
    const code = await formatCode(bundleCode, {parser: 'typescript'})
    // fs.writeFileSync(allIconsPath, bundleCode.trim() + "\n");
}

async function generateIconMap(options){
    const {srcDir, pkgName, setName, globalNames} = options
    const iconsMapContent = `
        /**
        * Auto-generated icons map for ${setName}
        * Enables lazy loading via dynamic imports
        */

        // Dynamic imports for all icons
        export const ICONS = {
        ${globalNames.map(iconKey => `  "${iconKey}": () => import('./icons/${iconKey}.js'),`).join('\n')}
        } as Record<string, () => Promise<{ default: any }>>;

        // Helper function to get icon dynamically
        export async function getIcon(iconName: string) {
            if (ICONS[iconName]) {
            const module = await ICONS[iconName]();
            return module.default;
            }
            throw new Error(\`Icon \${iconName} not found\`);
        }

        // Export all icon names for type safety
        export const iconNames = [${globalNames.map(k => `"${k}"`).join(', ')}] as const;

        export type IconName = typeof iconNames[number];
    `;
    const code = await formatCode(iconsMapContent, {parser: 'typescript'})
    // fs.writeFileSync(path.join(srcDir, "icons-map.ts"), iconsMapContent.trim() + "\n");
}
