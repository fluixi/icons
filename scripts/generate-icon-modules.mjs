function sanitizeName(name) {
    return name.replace(/\.svg$/, "").replace(/[^a-zA-Z0-9_-]/g, "");
}

function toPascalCase(str) {
    return str
        .replace(/[-_]+/g, " ")
        .replace(/[^\w\s]/g, "")
        .split(/\s+/)
        .filter(word => word.length > 0) // Remove empty strings
        .map(word => {
            if (word.length === 1) {
                return word.toUpperCase(); // Single letters should be uppercase
            }
            return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
        })
        .join('');
}

function getLicense(pkgName) {
    const info = LICENSES[pkgName];
    if (!info)
        return `Unknown License Source (${pkgName})`;

    const { name, license, url, copyright } = info;
    return `${name} — ${license}${url ? ` (${url})` : ""}${copyright ? ` ${copyright}` : ""}`;
}

function insertLicense(svg, licenseText) {
  const cleanLicense = licenseText.replace(/--/g, "–"); // prevent comment break
  return svg.replace(/>/, `>\n<!-- ${cleanLicense} -->\n`)
    // ? `<!-- ${cleanLicense} -->\n${svg}`
    // : svg.replace(/^<svg/, `<!-- ${cleanLicense} -->\n<svg`);
    // : svg.replace(/^>/, `>\n<!-- ${cleanLicense} -->\n`);
}

function optimizeSVG(svg) {
    const result = optimize(svg, {
        multipass: true,
        plugins: [
            "removeDimensions",
            "removeXMLNS",
            "removeMetadata",
            "removeEditorsNSData",
            "cleanupAttrs",
            "convertStyleToAttrs",
            "collapseGroups",
            "removeEmptyContainers",
            "mergePaths",
            {
                name: "removeAttrs",
                params: { attrs: "(stroke-linejoin|stroke-linecap)" },
            },
        ],
    });
    return result.data;
}

