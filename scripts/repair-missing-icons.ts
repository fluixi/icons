/**
 * repair-missing-icons.ts
 *
 * Safety net for rare generator glitches where an index.ts imports a per-icon
 * module that was never written (e.g. an SVG→string conversion produced an
 * empty file). Finds every `import X from "./slug.js"` whose `slug.ts` is
 * missing, then regenerates it from the source SVG, matching sibling format.
 *
 * Usage:  tsx scripts/repair-missing-icons.ts
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const ICONS = path.join(ROOT, "packages", "icons");

/** Strip xmlns/width/height from the outer <svg>, keep viewBox + inner content. */
function normalizeSvg(svg: string): string {
  return svg
    .replace(/<\?xml[^>]*\?>/g, "")
    .replace(/<svg([^>]*)>/i, (_m, attrs: string) => {
      const viewBox = attrs.match(/viewBox="[^"]*"/i)?.[0] ?? 'viewBox="0 0 24 24"';
      return `<svg ${viewBox}>`;
    })
    .trim();
}

/** Pull the `/** … *​/` license header + comment from a sibling generated file. */
function siblingHeader(dir: string, excludeSlug: string): { header: string; comment: string } {
  for (const f of fs.readdirSync(dir)) {
    if (!f.endsWith(".ts") || f === "index.ts" || f === `${excludeSlug}.ts`) continue;
    const content = fs.readFileSync(path.join(dir, f), "utf8");
    const license = content.match(/License: (.+)/)?.[1]?.trim();
    if (license) return { header: license, comment: license };
  }
  return { header: "", comment: "" };
}

function findSourceSvg(pkgDir: string, variantPath: string, slug: string): string | null {
  const dir = path.join(pkgDir, "icons", variantPath);
  if (!fs.existsSync(dir)) return null;
  for (const candidate of [slug, slug.replace(/-/g, "_")]) {
    const p = path.join(dir, `${candidate}.svg`);
    if (fs.existsSync(p)) return p;
  }
  return null;
}

export function repairMissingIcons(): { repaired: number; failed: number } {
  let repaired = 0;
  let failed = 0;

  for (const pkg of fs.readdirSync(ICONS)) {
  const pkgDir = path.join(ICONS, pkg);
  const iconsSrc = path.join(pkgDir, "src", "icons");
  if (!fs.existsSync(iconsSrc)) continue;

  const indexes: string[] = [];
  const walk = (d: string) => {
    for (const e of fs.readdirSync(d, { withFileTypes: true })) {
      const full = path.join(d, e.name);
      if (e.isDirectory()) walk(full);
      else if (e.name === "index.ts") indexes.push(full);
    }
  };
  walk(iconsSrc);

  for (const index of indexes) {
    const dir = path.join(index, "..");
    const content = fs.readFileSync(index, "utf8");
    const re = /import (\w+) from "\.\/([\w-]+)\.js"/g;
    let m: RegExpExecArray | null;
    while ((m = re.exec(content))) {
      const [, varName, slug] = m;
      if (slug === "index") continue;
      // Repair if the referenced file is missing OR empty (0 bytes) — the
      // generator occasionally writes an empty module when SVG conversion fails.
      const tsPath = path.join(dir, `${slug}.ts`);
      if (fs.existsSync(tsPath) && fs.statSync(tsPath).size > 0) continue;

      const variantPath = path.relative(iconsSrc, dir);
      const svgPath = findSourceSvg(pkgDir, variantPath, slug);
      if (!svgPath) {
        console.warn(`  ✗ ${pkg}/${variantPath}/${slug}: no source SVG found`);
        failed++;
        continue;
      }

      const svg = normalizeSvg(fs.readFileSync(svgPath, "utf8"));
      const { comment } = siblingHeader(dir, slug);
      const body = comment
        ? svg.replace(/(<svg [^>]*>)/, `$1\n<!-- ${comment} -->`)
        : svg;

      const file = [
        `/**`,
        ` * Auto-generated by generate-icon-modules.ts (repaired)`,
        ` * Icon: ${varName}`,
        comment ? ` * License: ${comment}` : ` *`,
        ` */`,
        `const ${varName} = \`${body}\`;`,
        `export default ${varName};`,
        `export { ${varName} };`,
        `export type IconName = '${slug}';`,
        ``,
      ].join("\n");

      fs.writeFileSync(path.join(dir, `${slug}.ts`), file);
      console.log(`  ✓ repaired ${pkg}/${variantPath}/${slug}.ts`);
      repaired++;
    }
  }
  }

  return { repaired, failed };
}

// CLI
if (import.meta.url === `file://${process.argv[1]}`) {
  console.log("🩹 Repairing missing/empty icon modules…");
  const { repaired, failed } = repairMissingIcons();
  console.log(`\n✅ Repair done — ${repaired} regenerated, ${failed} unresolved.`);
  if (failed > 0) process.exit(1);
}
